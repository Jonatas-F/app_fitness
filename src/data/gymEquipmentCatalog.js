function slugify(value) {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

export const gymEquipmentCatalog = equipmentCategories.map((category) => ({
  ...category,
  items: category.items.map((name) => ({
    id: `${category.id}-${slugify(name)}`,
    name,
    image: null,
  })),
}));

export const allGymEquipment = gymEquipmentCatalog.flatMap((category) =>
  category.items.map((item) => ({
    ...item,
    categoryId: category.id,
    categoryTitle: category.title,
  }))
);
